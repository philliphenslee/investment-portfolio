/**
 * @description Adds two numbers and return sum
 * @param x
 * @param y
 */
const sum = function(x: number, y: number): number {
  return x + y
}

/**
 * @description Multiply two numbers
 * @param x
 * @param y
 */
const multiply = function(x: number, y: number) {
  return x * y
}

export default sum
export { multiply }
